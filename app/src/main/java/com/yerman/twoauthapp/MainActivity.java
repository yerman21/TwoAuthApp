    public boolean changeStatus(boolean band_change){
        boolean band = false;
        try {
            db.collection("UserSession").document(txt_user_name.getText().toString())
                    .update("auth_band", band_change);
            band = true;
        }catch (Exception e) {
            System.out.println(e.getMessage());
        }
        return band;
    }



